function getCurrentLang() {
  const stored = localStorage.getItem('language');
  if (stored && TRANSLATIONS[stored]) return stored;
  const browser = (navigator.language || '').slice(0, 2);
  return TRANSLATIONS[browser] ? browser : 'en';
}

